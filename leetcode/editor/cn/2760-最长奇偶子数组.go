func longestAlternatingSubarray(nums []int, threshold int) int {
	maxVal := func(a, b int) int {
		if b > a {
			return b
		}
		return a
	}
	max, n := 0, len(nums)
	for i := 0; i < n; {
		if nums[i]&1 == 1 || nums[i] > threshold {
			i++
			continue
		}
		j := i + 1
		for ; j < n; j++ {
			if ((j-i)^nums[j])&1 == 1 || nums[j] > threshold {
				break
			}
		}
		max = maxVal(max, j-i)
		i = j
	}
	return max
}

