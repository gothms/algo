func maxHeight(cuboids [][]int) int {
	// dpï¼šç±»ä¼¼ lc-300 lc-354
	for _, c := range cuboids {
		sort.Ints(c) // é•¿å®½é«˜æ’åº
	}
	sort.Slice(cuboids, func(i, j int) bool { // ä¸‰ä¸ªå€¼éƒ½è¦ä¸¥æ ¼æ’åºï¼šdp çš„é‡ç‚¹æ¡ä»¶
		return cuboids[i][0] < cuboids[j][0] ||
			cuboids[i][0] == cuboids[j][0] && (cuboids[i][1] < cuboids[j][1] ||
				cuboids[i][1] == cuboids[j][1] && cuboids[i][2] < cuboids[j][2])
	})
	var ans int
	dp := make([]int, len(cuboids))
	for i, c := range cuboids {
		for j, p := range cuboids[:i] {
			if c[1] >= p[1] && c[2] >= p[2] { // è¢«å®¹çº³
				dp[i] = max(dp[i], dp[j]) // çŠ¶æ€è½¬ç§»
			}
		}
		dp[i] += c[2]
		ans = max(ans, dp[i])
	}
	return ans
}

