func convertToTitle(columnNumber int) string {
	var buf []byte
	for ; columnNumber > 0; columnNumber = (columnNumber - 1) / 26 {
		buf = append(buf, 'A'+byte((columnNumber-1)%26))
	}
	for i, j := 0, len(buf)-1; i < j; i, j = i+1, j-1 {
		buf[i], buf[j] = buf[j], buf[i]
	}
	return string(buf)
}

